            <h3>Movement and capture</h3>
            <p>
                The two players move their checkers in opposing directions. </br>
                After rolling the dice, players must, if possible, move their checkers according to the number shown on each die. For example, if the player rolls a 6 and a 3 (denoted as "6-3"), the player must move one checker six points forward, and another or the same checker three points forward. The same checker may be moved twice, as long as the two moves can be made separately and legally: six and then three, or three and then six. 
            </p>
            <p>
                In the course of a move, a checker may land on any point that is unoccupied or is occupied by one or more of the player's own checkers. It may also land on a point occupied by exactly one opposing checker, or "blot". In this case, the blot has been "hit" and is placed in the middle of the board on the bar that divides the two sides of the playing surface. A checker may never land on a point occupied by two or more opposing checkers; thus, no point is ever occupied by checkers from both players simultaneously.
            </p>
            <h3>Game ending</h3>
            <p>
                When all of a player's checkers are in that player's home board, that player may start removing them; this is called "bearing off".
                To bear off, the player must roll the dice, and move each chip out of the board, in the same way as moved chips in the home board. The "home point" is out of the board, next to the point 24 and to the point 1. 
                It could be represented as point 0 or point 25.
            </p>
